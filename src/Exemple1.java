public class Exemple1 {
    public static void main(String[] args) {
       //the byte data type is used to store a whole number between -128 and 127
       byte b = 100; // Example of byte
       System.out.println("Value of b: " + b);
       //the short data type is used to store a whole number between -32,768 and 32,767
       short s = 10000; // Example of short
       System.out.println("Value of s: " + s);
       //the int data type is used to store a whole number between -2,147,483,648 and 2,147,483,647
         int i = 100000; // Example of int
         System.out.println("Value of i: " + i);
       //the long data type is used to store a whole number between -9,223,372,036,854,775,808 and 9,223,372,036,854,775,807
       long l = 10000000000L; // Example of long
       System.out.println("Value of l: " + l);
       //the float data type is used to store a decimal number with single precision
       float f = 10.5f; // Example of float daoriri mn f cz ay7sbha double
       System.out.println("Value of f: " + f);
       //the double data type is used to store a decimal number with double precision
       double d = 10.5; // Example of double
       System.out.println("Value of d: " + d);
       //the char data type is used to store a single character
       char c = 'A'; // Example of char
       System.out.println("Value of c: " + c);
       //the unicode character set is used to represent characters in Java
       char unicodeChar = 65; // Example of unicode character
       System.out.println("Unicode character: " + unicodeChar);
       //the boolean data type is used to store a true or false value
       boolean isJavaFun = true; // Example of boolean
       System.out.println("Is Java fun? " + isJavaFun);
    }
}
